package com.example.apis_using_voley;

public class Student {

    public String StudentId;
    public String Name;
    public String Phone;
    public String Email;
    public String Password;
    public String Image;

    public Student(String studentId, String name, String phone, String email, String password, String image) {
        StudentId = studentId;
        Name = name;
        Phone = phone;
        Email = email;
        Password = password;
        Image = image;
    }
}
